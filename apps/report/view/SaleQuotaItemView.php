    public function evaluation(array $params,$date){
        if(!is_array($params)){
            echo('参数错误');
            exit;
        }
       $dateCount=date('t', strtotime($date));//获取每个月的天数
       $record=[];
       $el=$this->arr;
       for ($i=0;$i<count($el);$i++){
          $elements=$el[$i];
          $month=[];
          for($j=0;$j<$dateCount+1;$j++){ //循环天数+1
              $month[]=$params[$elements[$j]];
          }
           $record[]=$month;
       }

       return  array(
               '1'=>[$record[0]],
               '2'=>[$record[1],$record[2]],
               '3'=>[$record[3]],
               '4'=>[$record[4]],
               '5'=>[$record[5]],
               '6'=>[$record[6]],
               '8'=>[$record[7],$record[8]],
               '9'=>[$record[9]],
               '10'=>[$record[10], $record[11]],
